// Function to display one character at a time, updating every second
function displayCharactersOneByOne(charactersArray) {
  const characterContainer = document.getElementById('characterContainer');
  let index = 0;
  let intervalDuration = 800;

  // Clear previous content
  characterContainer.innerHTML = '';

  // Function to update character display
  function updateCharacter() {
    if (index < charactersArray.length) {
      characterContainer.innerHTML = '';
      const character = charactersArray[index];
      const characterElement = document.createElement('span');
      const count = document.createElement('p');

      count.textContent = index;
      count.style.fontSize = '20px';
      count.style.fontFamily = 'OpenSans';

      characterElement.textContent = character;
      characterElement.style.fontSize = '300px';

      characterContainer.appendChild(characterElement);
      characterContainer.appendChild(count);

      index++;
      intervalDuration *= 0.98;
      clearInterval(intervalId);
      intervalId = setInterval(updateCharacter, intervalDuration);
    } else {
      clearInterval(intervalId); // Stop interval when all characters are displayed
    }
  }
  let intervalId = setInterval(updateCharacter, intervalDuration);
  // Initial call to display the first character
  updateCharacter();

  // Set interval to update character every second
}

// Display characters one by one in the DOM
displayCharactersOneByOne(japaneseCharacters);
